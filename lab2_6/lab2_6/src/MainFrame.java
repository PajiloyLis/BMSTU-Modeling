    private void Service1DistributionComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Service1DistributionComboBoxActionPerformed
        // TODO add your handling code here:
        try{
            String distributionName = Objects.requireNonNull(Service1DistributionComboBox.getSelectedItem()).toString();
            DistributionsType serviceDistribution = DistributionsType.fromDisplayName(distributionName);
            
            switch(serviceDistribution){
                case DistributionsType.UNIFORM -> {
                    Service1FirstParamLabel.setText("a:");
                    Service1FirstParamTextField.setEnabled(true);
                    Service1SecondParamLabel.setText("b:");
                    Service1SecondParamTextField.setEnabled(true);
                }
                case DistributionsType.NORMAL ->{
                    Service1FirstParamLabel.setText("mu:");
                    Service1FirstParamTextField.setEnabled(true);
                    Service1SecondParamLabel.setText("D[x]:");
                    Service1SecondParamTextField.setEnabled(true);
                }
                case DistributionsType.EXPONENTIAL ->{
                    Service1FirstParamLabel.setText("lambda:");
                    Service1FirstParamTextField.setEnabled(true);
                    Service1SecondParamLabel.setText("");
                    Service1SecondParamTextField.setEnabled(false);
                }
                case DistributionsType.POISSON ->{
                    Service1FirstParamLabel.setText("lambda:");
                    Service1FirstParamTextField.setEnabled(true);
                    Service1SecondParamLabel.setText("");
                    Service1SecondParamTextField.setEnabled(false);
                }
                case DistributionsType.ERLANG -> {
                    Service1FirstParamLabel.setText("k:");
                    Service1FirstParamTextField.setEnabled(true);
                    Service1SecondParamLabel.setText("lambda:");
                    Service1SecondParamTextField.setEnabled(true);
                }
            }
            
        }
        catch(NullPointerException e)
        {
            return;
        }
        catch(Exception e)
        {
            return;
        }   
    }//GEN-LAST:event_Service1DistributionComboBoxActionPerformed

